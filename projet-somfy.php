					<p class="video-text">

						Somfy est un groupe industriel français spécialisé dans la motorisation et l’automatisation des ouvertures de l’habitat, du bâtiment ainsi que du système d’alarme. Considéré comme le leader mondial dans ces domaines, Somfy est une entreprise qu’on ne présente plus.
						<br>
						<br>
						En Novembre 2016, après 2 mois de formation, il était temps pour nous de connaître le client de notre première semaine de projet. Somfy s’est proposé de nous offrir l’opportunité d’être challengés sur un projet de web application en partenariat avec l’agence de communication Waoup.
						<br>
						<br>
						L’objectif était de créer une expérience utilisateur (UX) ainsi qu’une maquette design (UI) d’une application permettant d’imaginer à quoi pourrait ressembler « la fenêtre de nos rêves » avec un système d’automatisation Somfy. Le tout grâce à l’aide d’une technologie de réalité augmentée via l’appareil photo de notre smartphone. Avant de commencer, Waoup nous a transmis notre premier brief client, nous découvrions alors pour la première fois, les enjeux d’un cahier des charges à respecter, sans pour autant perdre de vue le but de surprendre notre premier client.
						<br>
						<br>
						Divisé en groupes de 4, nous nous sommes attelés à la tâche. Dans un premier temps, Caroline et moi avons travaillé sur la conception de l’UX sur brouillon, pendant que Cyril cherchait un outil informatique de conception UX. Dans un deuxième temps, Cyril et Lola travaillaient sur la maquette UX/UI finale, pendant que Caroline et moi étions en train de mettre en place notre présentation, ainsi que le livrable papier final.
						<br>
						<br>
						Au terme de cette semaine riche, nous avons pu nous familiariser avec des termes tels que « Wireframe », « Mock Up », ou encore « UX ». Nous avons aussi soumis notre proposition à un client pour la première fois. Ce projet est donc en quelque sorte le début de mon histoire avec le monde du digital.
						<br>
						<br>
						Cette semaine à été difficile car nous abordions concrètement beaucoup de thématiques jamais vues auparavant en respectant un timing imparti. Que ce soit dans la gestion de projet, dans le maquettage sur ordinateur, ou même simplement le fait de convaincre. Nous avons réussi, puisque Somfy a répondu favorablement à notre proposition.
						<br>
						<br>
						Ce premier projet s’est donc terminé sur un happy end, ainsi qu’un week-end bien mérité.
 

 


						
